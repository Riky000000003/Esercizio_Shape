package org.example;

public class Cpoint
{
    private double asseX;
    private double asseY;

    public Cpoint(double asseX, double asseY)
    {
        this.asseX = asseX;
        this.asseY = asseY;
    }

    public double getAsseX() {
        return asseX;
    }

    public double getAsseY() {
        return asseY;
    }
}
